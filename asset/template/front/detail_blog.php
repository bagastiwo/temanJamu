						<h2 class="mb-3">Manfaat Jamu Sepanjang Pandemi Covid-19, Begini Buat Jamu Beras Kencur</h2>
            <p>Dikutip dari TEMPO.CO, Jakarta - Jamu merupakan minuman herbal hasil dari beberapa kombinasi rempah-rempah yang kaya akan gizi bermanfaat. Indonesia sendiri tingkat konsumsi jamu termasuk masif pada setiap elemen masyarakat.<p>
              <img src="images/berita1.jpg" alt="" class="img-fluid">
            </p>
            <p>Menurut data dari Peneliti Balai Besar Penelitian dan Pengembangan Tanaman Obat dan Obat Tradisional sepanjang masa Covid-19 sejak April hingga Mei 2020, penelitian ini menyebutkan konsumsi jamu meningkat.
            <p>Datanya,  79 persen responden mengonsumsi jamu untuk meningkatkan daya tahan tubuh selama wabah Covid-19. Jenis obat herbal yang dikonsumsi rimpang jenis jahe sebanyak 1.252 orang,  kunyit sebanyak 1.057 orang Serai sebanyak 969 orang, temulawak 754 orang dan kayu manis sebanyak 647 orang</p>
            <p>Riset Tumbuhan Obat dan Jamu (Ristoja) yang dilaksanakan oleh Balai Besar Litbang Tanaman Obat dan Obat Tradisional, telah menghasilkan database pengetahuan etnofarmakologi informasi ramuan 33.000 ramuan obat tradisional yang secara empiris terbukti mampu menjaga kesehatan masyarakat, terdiri dari 2.800 spesies tumbuhan obat.</p>
            <p>Pengembangnya sendiri, menurut Peraturan Menteri Kesehatan No. 003/MENKES/PER/I/2010 diatur secara khusus terkait Saintifikasi Jamu, yakni obat tradisional di Indonesia terutama di sarana pelayanan kesehatan.</p>
            <p>Tujuannya Saintifikasi Jamu ini sendiri sebagai pembuktian berdasarkan landasan ilmiah (evidenced based) melalui penelitian yang dilakukan di sarana pelayanan kesehatan terhadap ramuan jamu.</p>
            <p>Mengonsumsi jamu sendiri, kini  memiliki jargon tersendiri berbunyi “Back to nature”, merupakan upaya mensosialisakan kembali rutinitas konsumsi jamu. Salah satu jamu yang populer dikonsumsi oleh masyarakat Indonesia ada jamu beras kencur.</p>
            <p>Cara pembutan jamu beras kencur relatif mudah yakni hanya membutuhkan, beras dan beberapa rimpang kencur.  Tahap awal yang mesti dilakukan adalah meyiapkan bahan utama seperti kencur dan beras, ada juga bahan tambahan lain seperti gula Jawa, gula pasir, jahe, asam Jawa.</p>
            <p>Kencur merupakan tanaman yang mengandung protein, serat, zat besi, dan zinc, penjelasan ini disampaikan oleh Toxicology Reports. Cara mengolah jamu beras kencur yakni rendam beras selama 3 jam atau semalaman lebih bagus, menunggu beras lunak, rebus air, kencur, jahe, asam jawa, gula Jawa, dan gula pasir hingga mendidih.</p>
            <p>Ketika tekstur beras melunak, hancurkan beras menggunakan blender ataupun alat penggiling lainnya. Setelah proses pelembutan beras, campurkan beras yang sudah dilembutkan ke dalam rebusan jamu, aduk hingga rata. Pindahkan jamu beras kencur serta menggunakan saringan dan air jamu dipindahkan ke dalam botol atau gelas.</p>
            <p>Jamu bisa disimpan dalam botol tertutup, hangatkan kembali saat ingin mengonsumsinya, ada baiknya jika diletakkan di dalam suhu ruang.  Ada baiknya jika mengonsumsi jamu dilakukan setalah jamu didiamkan selama satu malam untuk rasa terbaik.</p>
            <p>Dalam jamu beras kencur menurut International Journal of Pharmacy and Pharmaceutical Sciences kandungan kencur berkhasiat untuk mengatasi permasalah pencernaan seperti sakit perut, diare. Selain itu, kencur terbukti dapat hambat bakteri Lactobacillus acidophilus berperan sebabkan karies gigi. Jadi, gangguan pada mulut dan gigi dapat dibantu dengan mengonsumsi jamu beras kencur.</p>
